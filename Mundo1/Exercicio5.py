# Crie um programa que peça ao usuário e mostre o dobro, triplo e a raiz quadrada.

print('=-' * 7, 'DESAFIO 5', '=-'* 7)
n = int(input('Digite um número: '))
print('O número informado é {}.'.format(n))
print('O dobro deste número é {}.'.format(n * 2))
print('O triplo deste número é {}.'.format(n *3))
print('A raiz quadrada deste número é {}.'.format(n ** (1/2)))

